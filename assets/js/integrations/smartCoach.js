let enabled = localStorage.getItem('smart_coach_enabled') !== '0';

function get(id) { return document.getElementById(id); }
function esc(value) { return String(value ?? '').replace(/[&<>"']/g, (ch) => ({ '&': '&amp;', '<': '&lt;', '>': '&gt;', '"': '&quot;', "'": '&#39;' }[ch])); }
function loadCss() {
  if (document.querySelector('link[href="./assets/css/smart-coach.css"]')) return;
  const link = document.createElement('link');
  link.rel = 'stylesheet';
  link.href = './assets/css/smart-coach.css';
  document.head.appendChild(link);
}
function currentStepIndex() {
  const counter = get('smartWizardCounter')?.textContent || '';
  const match = counter.match(/Шаг\s+(\d+)/i);
  return match ? Math.max(0, Number(match[1]) - 1) : 0;
}
function list(items) { return '<ul>' + (items || []).map((item) => `<li>${esc(item)}</li>`).join('') + '</ul>'; }
function ensureCoach() {
  if (get('smartCoach')) return;
  const shell = get('smartWizardShell');
  if (!shell) return;
  const box = document.createElement('div');
  box.id = 'smartCoach';
  box.className = 'smart-coach';
  shell.insertAdjacentElement('afterend', box);
  const toggle = document.createElement('button');
  toggle.id = 'btnSmartCoachOpen';
  toggle.type = 'button';
  toggle.className = 'smart-coach-open';
  toggle.textContent = 'Обучение новичка';
  toggle.onclick = () => {
    enabled = !enabled;
    localStorage.setItem('smart_coach_enabled', enabled ? '1' : '0');
    renderCoach();
  };
  get('btnSmartWizardToggle')?.insertAdjacentElement('afterend', toggle);
}
function renderCoach() {
  document.body.dataset.smartCoach = enabled ? '1' : '0';
  const box = get('smartCoach');
  if (!box) return;
  const step = COACH[currentStepIndex()] || COACH[0];
  box.hidden = !enabled;
  get('btnSmartCoachOpen').textContent = enabled ? 'Скрыть обучение' : 'Обучение новичка';
  if (!enabled) return;
  box.innerHTML = `
    <div class="smart-coach-head">
      <div>
        <h3>🎓 Подсказки для новичка <span class="smart-coach-mode">можно скрыть</span></h3>
        <p>Коротко: зачем этот шаг, что спросить и где чаще всего ошибаются.</p>
      </div>
      <button id="btnSmartCoachHide" type="button" class="smart-coach-toggle">Скрыть обучение</button>
    </div>
    <div class="smart-coach-grid">
      <div class="smart-coach-card blue"><b>Зачем это нужно</b>${list(step.why)}</div>
      <div class="smart-coach-card green"><b>Что спросить у клиента</b>${list(step.ask)}</div>
      <div class="smart-coach-card"><b>Частые ошибки</b>${list(step.mistakes)}</div>
      <div class="smart-coach-card red"><b>Когда подключать юриста</b>${list(step.lawyer)}</div>
      <div class="smart-coach-card red"><b>Что нельзя обещать</b>${list(step.dont)}</div>
    </div>
    <div class="smart-coach-mini">Опытный СПН может скрыть этот блок. Настройка сохранится на этом компьютере.</div>
  `;
  get('btnSmartCoachHide').onclick = () => {
    enabled = false;
    localStorage.setItem('smart_coach_enabled', '0');
    renderCoach();
  };
}
function start() {
  loadCss();
  ensureCoach();
  renderCoach();
  document.addEventListener('click', () => setTimeout(renderCoach, 120));
  document.addEventListener('input', () => setTimeout(renderCoach, 120));
  document.addEventListener('change', () => setTimeout(renderCoach, 120));
}
let attempts = 0;
const timer = setInterval(() => {
  attempts += 1;
  if (get('smartWizardShell') && get('smartWizardCounter')) {
    clearInterval(timer);
    start();
  }
  if (attempts > 80) clearInterval(timer);
}, 200);
